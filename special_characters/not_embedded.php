	<span class="heading-2">Special Characters</span>
    <hr/>
	<p>The transcription of  manuscripts from the period of <em>The Book of Margery Kempe</em> (i.e. circa 1430) is frequently problematized by their scribes' use of "special characters" and abbreviations for which there may not yet exist standardized typographical character support even among typographical catalogs as expansive as <a href="http://www.unicode.org" target="_blank">Unicode</a>. Fortunately, working groups  of medievalists, font designers, and Web developers, like the <a href="http://www.mufi.info" target="_blank">Medieval Unicode Font Initiative (MUFI)</a>, have worked to develop typographical character support for many of these special characters and abbreviations&#x2014;recommending them to medivialists as a supplement to Unicode and to the Unicode Consortium for inclusion in its catalog.</p>
	<p>To reproduce the many special characters and abbreviations found in <em>The Book of Margery Kempe</em> as faithfully and sytematically as possible, this project  supplements its use of Unicode characters with vetted non-Unicode characters found in  MUFI's <a href="http://www.mufi.info/specs" target="_blank"><em>Character Recommendations</em></a>.</p>
	<p>The 1,500+ characters included in the <em>Recommendations</em> are supported by several freely available fonts, including <a href="../software.php" target="_blank">Andron Scriptor Web</a>, which this project, through collaborations with the font's designer and MUFI, embeds in its online transcriptions of <em>The Book of Margery Kempe</em>.</p>
	<p>The remainder of this page was rendered using the embedded Andron Scriptor Web font and a combination of Unicode and MUFI characters. To see what this page would look like without the embedded font, click the off button below.</p>
	<p>To learn more about how this project embeds fonts, click <a href="../code.php" target="_blank">here</a>.</p>
